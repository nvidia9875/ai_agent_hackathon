// 犬種間の類似度スコア（0-1）
export function getBreedSimilarityScore(breed1: string | undefined, breed2: string | undefined): number {
  // 両方とも犬種が指定されていない場合
  if (!breed1 || !breed2 || breed1 === '不明' || breed2 === '不明') {
    return 0.3; // 低いスコアだが、完全に0ではない
  }
  
  // 完全一致
  if (breed1 === breed2) {
    return 1.0; // 最高スコア
  }
  
  // ミックス犬の場合
  if (breed1 === 'ミックス（雑種）' || breed2 === 'ミックス（雑種）') {
    return 0.4; // 中間的なスコア
  }
  
  // その他の場合
  if (breed1 === 'その他' || breed2 === 'その他') {
    return 0.3;
  }
  
  // サイズカテゴリーによる類似度判定
  const smallBreeds = ['トイプードル', 'チワワ', 'ミニチュアダックスフンド', 'ポメラニアン', 'ヨークシャーテリア', 'マルチーズ', 'シーズー', 'パグ', 'フレンチブルドッグ', 'ボストンテリア', 'キャバリア', 'パピヨン', 'ペキニーズ', 'ミニチュアピンシャー', 'イタリアングレーハウンド'];
  const mediumBreeds = ['柴犬', 'コーギー', 'ビーグル', 'コッカースパニエル', 'ボーダーコリー', 'シェルティ', 'ブルテリア', 'バセットハウンド', 'ダルメシアン', 'ウィペット', '紀州犬', '甲斐犬', '四国犬', '北海道犬'];
  const largeBreeds = ['ゴールデンレトリーバー', 'ラブラドールレトリーバー', 'シベリアンハスキー', 'ジャーマンシェパード', 'ドーベルマン', 'ロットワイラー', 'ボクサー', 'セントバーナード', 'グレートデーン', 'バーニーズマウンテンドッグ', 'ニューファンドランド', 'サモエド', 'アラスカンマラミュート', 'スタンダードプードル', 'コリー', 'オールドイングリッシュシープドッグ', '秋田犬'];
  
  const getSize = (breed: string): 'small' | 'medium' | 'large' | 'unknown' => {
    if (smallBreeds.includes(breed)) return 'small';
    if (mediumBreeds.includes(breed)) return 'medium';
    if (largeBreeds.includes(breed)) return 'large';
    return 'unknown';
  };
  
  const size1 = getSize(breed1);
  const size2 = getSize(breed2);
  
  // 同じサイズカテゴリーなら少し高めのスコア
  if (size1 === size2 && size1 !== 'unknown') {
    return 0.2;
  }
  
  // 異なる犬種の場合は低いスコア
  return 0.1;
}